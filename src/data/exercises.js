export function getSeriesRepsValue(volume, nivel, field) {
  const config = volumeConfig[volume]?.[nivel];
  if (!config) return field === 'series' ? 3 : 12;
  const val = config[field];
  if (typeof val === 'string' && val.includes('-')) {
    return parseInt(val.split('-')[0], 10);
  }
  return typeof val === 'number' ? val : parseInt(val, 10) || 3;
}

export function getExerciciosCount(volume, grupo) {
  const counts = exerciciosPorGrupoCount[grupo]?.[volume];
  if (!counts) return { min: 2, max: 3 };
  return counts;
}

export function getCardioRecomendacao(objetivo) {
  return cardioRecomendacao[objetivo] || cardioRecomendacao['Saúde geral'];
}

export function gerarExercicios(volume, nivel, grupos, tipo) {
  const config = volumeConfig[volume]?.[nivel];
  if (!config) return [];

  const seriesVal = getSeriesRepsValue(volume, nivel, 'series');
  const repsVal = config.reps;

  let exercicios = [];
  let idCounter = 1;

  for (const grupo of grupos) {
    // Cardio exercises are handled differently
    if (grupo === 'Cardio') {
      continue;
    }

    const pool = exerciciosPorGrupo[grupo] || [];
    if (pool.length === 0) continue;

    const countInfo = getExerciciosCount(volume, grupo);
    const count = Math.min(countInfo.max, pool.length);
    const selected = pool.slice(0, count);

    for (const ex of selected) {
      exercicios.push({
        id: tipo + '-' + idCounter,
        nome: ex.nome,
        grupoMuscular: grupo,
        tipo: 'forca',
        series: seriesVal,
        reps: repsVal,
        carga: '',
        concluido: false,
      });
      idCounter++;
    }
  }
  return exercicios;
}

export function gerarCardioDefault(objetivo) {
  const rec = getCardioRecomendacao(objetivo);
  return {
    id: 'cardio-auto',
    nome: 'Cardio',
    grupoMuscular: 'Cardio',
    tipo: 'cardio',
    duracao: rec.duracao,
    intensidade: rec.intensidade,
    concluido: false,
    recomendacao: rec,
  };
}
